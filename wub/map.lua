local spritesheet

function M.load()
	spritesheet = love.graphics.newImage('1sttileset.png')
	for j = 0, 10 do
		for i = 0, 3 do
			quad = love.graphics.newQuad(i * (tile_w), j * (tile_h), tile_w, tile_h, spritesheet:getDimensions())	
			table.insert(tiles,quad)
		end
	end

	M.canvas = love.graphics.newCanvas(800, 600)
end

function M.draw()
	love.graphics.setCanvas(M.canvas)
	love.graphics.clear(1, .9, .91)
	love.graphics.setColor(1, 1, 1)
	for j = 1, 19 do
		for i = 1, 19 do
			index = math.floor(math.random() * 20) + 1
			love.graphics.draw(spritesheet, tiles[ index], (i - 1) * (tile_w), (j - 1) * (tile_h))
		end
	end	
	love.graphics.setCanvas()
end

return M
